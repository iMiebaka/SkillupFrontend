class Arthmetric {
    add(value1, value2) {
      return parseFloat(value1) + parseFloat(value2);
    }
    divide(value1, value2) {
      return parseFloat(value1) / parseFloat(value2);
    }
    subtract(value1, value2) {
      return parseFloat(value1) - parseFloat(value2);
    }
    multiply(value1, value2) {
      return parseFloat(value1) * parseFloat(value2);
    }
  }
